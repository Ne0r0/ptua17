#Temperatūra pagal Celsijų: 20.0
# C = (F−32) × 5/9​

Farengeitai = input("Įveskite Farengeitus: ")
Farengeitai = float(Farengeitai)
Celcijus = (Farengeitai - 32) * 5 / 9

print("Temperatūra pagal Celcijų:", Celcijus)


